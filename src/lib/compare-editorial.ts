// ── エディトリアルテキスト生成 ──

export interface CompareEditorial {
  /** 魚種×タイプ固有の解説文（概要セクション拡張用） */
  contextParagraph: string;
  /** 「比較で見るべき3つの軸」 */
  selectionCriteria: string[] | null;
}

export function getCompareEditorial(
  fishName: string,
  typeName: string,
  totalCount: number,
): CompareEditorial {
  const fishCtx = FISH_CONTEXT[fishName];
  const typeCrit = TYPE_CRITERIA[typeName];

  // コンテキスト文の生成
  let contextParagraph: string;

  if (fishCtx) {
    contextParagraph =
      `${fishName}は${fishCtx.field}ターゲットだ。` +
      `${fishCtx.season}。` +
      `${typeName}を選ぶ際は${fishCtx.tip}。` +
      `CAST/LOGのデータベースには${totalCount}シリーズの${fishName}対応${typeName}が登録されており、` +
      `カラー展開・スペック・価格を横並びで比較することで最適な1本が見つかる。`;
  } else {
    contextParagraph =
      `${fishName}対応の${typeName}は${totalCount}シリーズが登録されている。` +
      `ターゲットのサイズやフィールドの条件に合わせたルアー選びが釣果を左右する。` +
      `カラー展開の多さはフィールドへの対応力の指標となり、スペックの違いを横並びで比較することで最適な選択ができる。`;
  }

  // 選び方の軸
  const selectionCriteria = typeCrit
    ? [typeCrit.key1, typeCrit.key2, typeCrit.key3]
    : null;

  return { contextParagraph, selectionCriteria };
}
