/**
 * スクレイパーのtype文字列をカノニカルタイプに正規化する。
 *
 * 1. すでにカノニカルならそのまま返す
 * 2. エイリアスマップにあれば変換して返す
 * 3. どちらでもなければ 'その他' を返し、警告ログを出す
 */
export function normalizeType(rawType: string): string {
  // 前後の空白を除去
  const trimmed = rawType.trim();

  // 1. カノニカルタイプならそのまま
  if (CANONICAL_TYPES.has(trimmed)) {
    return trimmed;
  }

  // 2. エイリアスマップで変換
  const mapped = TYPE_ALIAS_MAP[trimmed];
  if (mapped) {
    return mapped;
  }

  // 3. 未知のタイプ → その他 + 警告
  console.warn(`⚠️ normalizeType: 未知のタイプ "${trimmed}" → "その他" にフォールバック`);
  return 'その他';
}
